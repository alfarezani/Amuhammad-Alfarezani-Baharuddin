a = 9
b = 13

print('================== Besar dari 13')
hasil = a > 13
print(a, '> 13 adalah', hasil)
hasil = b > 13
print(b, '> 13 adalah', hasil)

print('================== Kecil dari 13')
hasil = a < 13
print(a, '< 13 adalah', hasil)
hasil = b < 13
print(b, '< 13 adalah', hasil)

print('================== Besar atau sama dari 13')
hasil = a >= 13
print(a, '>= 13 adalah', hasil)
hasil = b >= 13
print(b, '>= 13 adalah', hasil)

print('================== Kecil atau sama dari 13')
hasil = a <= 13
print(a, '<= 13 adalah', hasil)
hasil = b <= 13
print(b, '<= 13 adalah', hasil)

print('================== Sama dengan 13')
hasil = a == 13
print(a, '== 13 adalah', hasil)
hasil = b == 13
print(b, '== 13 adalah', hasil)

print('================== Tidak sama dengan 13')
hasil = a != 13
print(a, '!= 13 adalah', hasil)
hasil = b != 13
print(b, '!= 13 adalah', hasil)
